export function Navigation() {
  const pathname = usePathname();

  // Hide navigation during active workout
  if (pathname.startsWith('/workout/active')) {
    return null;
  }

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-[#111] border-t border-zinc-800 safe-area-bottom z-50">
      <div className="max-w-lg mx-auto flex justify-around items-center h-16">
        {navItems.map((item) => {
          const isActive = pathname === item.href ||
            (item.href !== '/' && pathname.startsWith(item.href));

          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                'flex flex-col items-center justify-center w-14 h-full transition-colors',
                isActive ? 'text-primary-400' : 'text-zinc-500 hover:text-zinc-300'
              )}
            >
              {item.icon}
              <span className="text-xs mt-1">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
